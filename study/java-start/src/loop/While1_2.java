package loop;

public class While1_2 {

    public static void main(String[] args) {

        int count = 0;
        while(count < 3){
            System.out.println("1 현재 숫자는 : " + count);
            count = count + 1;
            System.out.println("2 현재 숫자는 : " + count);
        }
        System.out.println("끝 숫자는 : 끝 "+ count);
    }

}//ckass
